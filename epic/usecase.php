    <h2>Eunice Use Case</h2>

    <p><strong>Overview:</strong> Eunice is looking for something to do this weekend with her gal pals.</p>

    <p><strong>When:</strong>From Home, Eunice will use her tablet or desktop to nav to the site. </p>

    <p><strong>Utilized Tech:</strong> As a result of Eunice's limited exposure to tech. She will be using the default web browser for her operating system.</p>

    <p><strong>Conclusions:</strong> Eunice will need a very simple and clear way to access the information she seeks. She is easily frustrated by overly complex systems. A max of three clicks should deliver her to her content. The content should also be Elegant (well designed but not overwhelming or flashy). Our site will also focus on being able to be delivered on browsers such as IE and Safari.</p>

    <h2>Interaction Flow</h2>
    <ol>
        <li>Eunice navigates to site</li>
        <li>Site returns home page</li>
        <li>Eunice searches for a specific venue</li>
        <li>Site directs Eunice to specific venue page</li>
        <li>Eunice reads info and clicks buy tickets link</li>
        <li>Site directs Eunice to external page</li>
    </ol>



